package com.logics;

class Pattern3{
public static void main(String[] arg){


for(int i=0;i<=20;i++){
	for(int j=0;j<=i+1;j++){
            System.out.print("*");
			      }
	System.out.println(" ");
		
}
for(int i=21;i>=0;i--){
	for(int j=0;j<=i-1;j++){
            System.out.print("*");
			      }
	System.out.println(" ");
		
}


}

}