package com.literals;

public class Literals {

	/**
	 * @Author: manjit
	 * @Company: waikhom
	 */
	public static void main(String[] args) {
		
		System.out.println("for int type");
		int i1=123;//integer
		int i2=012;//octal
		int i3=0x123;//hexadecimal
		System.out.println(i1);
		System.out.println(i2);
		System.out.println(i3);
		
		System.out.println("for float/double type");
		float f1=123.36f;
		double d1=123.78;
		double d2=99.99e-5;//99.99*10 to the power -5
		double d3=88.8E6;//88.8*10 to the power 6
		System.out.println(f1);
		System.out.println(d1);
		System.out.println(d2);
		System.out.println(d3);
		
		System.out.println("for character");
		char c1='a';
		char c2='A';
		char c3=97;
		char c4=65;
		System.out.println(c1);
		System.out.println(c2);
		System.out.println(c3);
		System.out.println(c4);

	}


}
